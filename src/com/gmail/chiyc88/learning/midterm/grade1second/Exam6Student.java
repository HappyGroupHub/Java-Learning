package com.gmail.chiyc88.learning.midterm.grade1second;

public class Exam6Student {
    private String name;
    private int age;

    public Exam6Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public void getName() {
        System.out.println(name);
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void getAge() {
        System.out.println(age);
    }
}
